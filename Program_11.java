public class Program_11 {
    public static void main(String[] args) {
        int a = 5;
        double d = (double) a / 2;
        System.out.println(d);

        int b = 2;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        int x = 5;
        System.out.println(x++);
        System.out.println(x--);

        x += 1;
        x -= 1;
        x *= 1;
        x /= 1;
    }
}